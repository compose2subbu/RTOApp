package com.virtusa.rto.newregistration;
public class Contact {
	private String firstname;
	private String lastname;
	private String gender;
	private String subtype;
	private String address;
	private long pincode;
	private long mobile;
	private String email;
	private String bankname;
	private long  insuranceno;
	private long  accountno;
	private long engineno;
	private long chasisno;
	private String doa;
	
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public long getPincode() {
		return pincode;
	}
	public void setPincode(long pincode) {
		this.pincode = pincode;
	}
	public long getMobile() {
		return mobile;
	}
	public void setMobile(long mobile) {
		this.mobile = mobile;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getBankname() {
		return bankname;
	}
	public void setBankname(String bankname) {
		this.bankname = bankname;
	}
	
	public long getAccountno() {
		return accountno;
	}
	public void setAccountno(long accountno) {
		this.accountno = accountno;
	}
	public long getEngineno() {
		return engineno;
	}
	public void setEngineno(long engineno) {
		this.engineno = engineno;
	}
	public long getChasisno() {
		return chasisno;
	}
	public void setChasisno(long chasisno) {
		this.chasisno = chasisno;
	}
	public String getDoa() {
		return doa;
	}
	public void setDoa(String doa) {
		this.doa = doa;
	}
	public long getInsuranceno() {
		return insuranceno;
	}
	public void setInsuranceno(long insuranceno) {
		this.insuranceno = insuranceno;
	}
	public String getSubtype() {
		return subtype;
	}
	public void setSubtype(String subtype) {
		this.subtype = subtype;
	}

}
